        try {
            places = PlaceController.getInstance().getPlaces();
        } catch (Exception ex) {
            Logger.getLogger(CustomerAUForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        jComboBox1.setModel(new DefaultComboBoxModel(places.toArray()));
