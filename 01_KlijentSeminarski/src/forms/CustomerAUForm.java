    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        try {
            // TODO add your handling code here:
            String name = txtName.getText();
            String last = txtLastname.getText();
            String number = txtNumber.getText();
            Place p = (Place) jComboBox1.getSelectedItem();

            if (name == null || last == null || number == null || name.isEmpty() || last.isEmpty() || number.isEmpty() || p == null) {
                JOptionPane.showMessageDialog(this, "System cannot save customer successfully!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            } else {
                JOptionPane.showMessageDialog(this, "System saved customer successfully!", "Info", JOptionPane.INFORMATION_MESSAGE);

            }
            Customer c = new Customer(name, last, number, p);
            CustomerController.getInstance().addCustomer(c);
//            customerForm.fillForm();
            JOptionPane.showMessageDialog(this, "System saved customer successfully!", "Info", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (Exception ex) {
            Logger.getLogger(CustomerAUForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);

        }


    }//GEN-LAST:event_jButtonAddActionPerformed

