    public void updateTblCustomer() {
        try {
            String s = txtCustomer.getText();
            List<Odo> l = CustomerController.getInstance().searchCustomer(s);
            jTable1.setModel(new TableModel(l));
        } catch (Exception ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void updateTblPlace() {
        try {
            Place place = (Place) jComboBox1.getSelectedItem();
            //List<Odo> l=CustomerController.getInstance().searchCustomer(place);

            Customer c = new Customer();
            c.setPlace(place);
            c.setName(txtCustomer.getText());
            List<Odo> l = CustomerController.getInstance().searchCustomer(c);
            jTable1.setModel(new TableModel(l));

//            TableModel tm = (TableModel) jTable1.getModel();
//        List<Odo> l = tm.getList();
            if (l != null && !l.isEmpty()) {
                if (jComboBox1.getSelectedIndex() != -1) {
                    JOptionPane.showMessageDialog(this, "System has found customers based on the given criteria", "Info", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "System has not found customers based on the given criteria", "Info", JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (Exception ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
