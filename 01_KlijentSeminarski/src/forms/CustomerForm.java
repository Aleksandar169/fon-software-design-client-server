    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            if (jTable1.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(this, "You must select item", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Customer c = (Customer) ((TableModel) (jTable1.getModel())).getSelectedItem(jTable1.getSelectedRow());
           // c = CustomerController.getInstance().getCustomer(c);
            if (c == null) {
                JOptionPane.showMessageDialog(this, "System cannot find customer", "Error", JOptionPane.ERROR_MESSAGE);                
            } else {
                JOptionPane.showMessageDialog(this, "System found customer", "Info", JOptionPane.INFORMATION_MESSAGE);                

            }
            CustomerAUForm c1 = new CustomerAUForm(this, c);
            c1.setVisible(true);
            c1.setLocationRelativeTo(null);
            JOptionPane.showMessageDialog(this, "System find customer", "Info", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "You must select item", "Error", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        Customer c = (Customer) ((TableModel) (jTable1.getModel())).getSelectedItem(jTable1.getSelectedRow());
        try {

            int i = JOptionPane.showConfirmDialog(rootPane, "Do you wont to delete " + c.getName() + " " + c.getLastname() + " from customers?", "Confirm deletion", JOptionPane.YES_NO_OPTION);
            if (i == JOptionPane.YES_OPTION) {
                CustomerController.getInstance().deleteCustomer(c);
                JOptionPane.showMessageDialog(this, "Customer is deleted!", "Info", JOptionPane.INFORMATION_MESSAGE);
                fillForm();
            } else {
                JOptionPane.showMessageDialog(this, "Customer is not deleted!", "Info", JOptionPane.INFORMATION_MESSAGE);
                return;
            }

        } catch (Exception ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

